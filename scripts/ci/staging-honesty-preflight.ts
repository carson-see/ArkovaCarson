// Report builder
// ---------------------------------------------------------------------------

export function buildReport(opts: {
  projectRef: string;
  migrationRows: MigrationRow[];
  submittedAnchorCount: number;
  prodVersions: string[];
  orgTopology?: OrgTopologyData;
  prodFacts?: ProdFactsData;
}): PreflightReport {
  const { projectRef, migrationRows, submittedAnchorCount, prodVersions } = opts;
  const checks: CheckResult[] = [];
  let allArtifactRows: MigrationRow[] = [];

  // Check 1: PR-only / staging-only rows
  const classified = migrationRows
    .map((row) => classifyMigrationRow(row))
    .filter((c): c is ArtifactClassification => c !== null);
  const artifactRowsFromClassification = classified.map((c) => c.row);
  checks.push({
    name: 'staging_only_rows',
    passed: classified.length === 0,
    details: classified.length === 0
      ? 'No PR-only or staging-only migration rows found.'
      : `${classified.length} suspect row(s): ${classified.map((c) => `${c.row.name} (${c.reason})`).join('; ')}`,
  });

  // Check 2: Duplicate names
  const dupeNames = findDuplicateNames(migrationRows);
  checks.push({
    name: 'duplicate_names',
    passed: dupeNames.length === 0,
    details: dupeNames.length === 0
      ? 'No duplicate migration names.'
      : `Duplicate name(s): ${dupeNames.join(', ')}`,
  });

  // Check 3: Duplicate versions
  const dupeVersions = findDuplicateVersions(migrationRows);
  checks.push({
    name: 'duplicate_versions',
    passed: dupeVersions.length === 0,
    details: dupeVersions.length === 0
      ? 'No duplicate migration versions.'
      : `Duplicate version(s): ${dupeVersions.join(', ')}`,
  });

  // Check 4: Known artifact rows
  const knownArtifacts = detectKnownArtifacts(migrationRows);
  checks.push({
    name: 'known_artifacts',
    passed: knownArtifacts.length === 0,
    details: knownArtifacts.length === 0
      ? 'No known artifact rows.'
      : `${knownArtifacts.length} known artifact(s): ${knownArtifacts.map((r) => r.name).join(', ')}`,
  });

  // Check 5: Missing SUBMITTED anchors
  checks.push({
    name: 'submitted_anchors',
    passed: submittedAnchorCount > 0,
    details: submittedAnchorCount > 0
      ? `${submittedAnchorCount} SUBMITTED anchor(s) found.`
      : 'Zero SUBMITTED anchors — environment may lack test fixtures.',
  });

  // Check 6: Prod divergence
  const divergence = computeProdDivergence(migrationRows, prodVersions);
  const prodDiverged = divergence.missingFromStaging.length > 0 || divergence.extraVsProd.length > 0;
  checks.push({
    name: 'prod_divergence',
    passed: !prodDiverged,
    details: prodDiverged
      ? `Missing from staging: [${divergence.missingFromStaging.join(', ')}]; Extra vs prod: [${divergence.extraVsProd.join(', ')}]`
      : 'Staging versions match prod ledger.',
  });

  // Check 7: Org topology (single-tenant prod vs multi-org staging seeds)
  if (opts.orgTopology) {
    checks.push(checkOrgTopology(opts.orgTopology));
  }

  // Check 8: Prod facts (pg_cron, function existence, scheduling)
  if (opts.prodFacts) {
    checks.push(checkProdFacts(opts.prodFacts));
  }

  // Collect all artifact rows (deduplicated by version+name).
  const seen = new Set<string>();
  allArtifactRows = [
    ...artifactRowsFromClassification,
    ...knownArtifacts,
  ].filter((row) => {
    const key = `${row.version}::${row.name}`;
    if (seen.has(key)) return false;
    seen.add(key);
    return true;
  });

  // Determine environment type.
  const anyFailed = checks.some((c) => !c.passed);
  const hasArtifacts = allArtifactRows.length > 0
    || dupeNames.length > 0
    || dupeVersions.length > 0
    || classified.length > 0;
  const missingAnchors = submittedAnchorCount === 0;

  let environmentType: EnvironmentType;
  if (!anyFailed) {
    environmentType = 'clean_mirror';
  } else if (hasArtifacts) {
    environmentType = 'soak_artifact';
  } else if (missingAnchors) {
    environmentType = 'fixture_seeded';
  } else {
    // Prod divergence only, no artifacts — still classify as soak_artifact.
    environmentType = 'soak_artifact';
  }

  return {
    environment_type: environmentType,
    staging_project_ref: projectRef,
    timestamp: new Date().toISOString(),
    checks,
    artifact_rows: allArtifactRows,
    missing_from_staging: divergence.missingFromStaging,
    extra_vs_prod: divergence.extraVsProd,
  };
}

