export function parseArgs(argv: string[]): ParsedArgs {
  let projectRef: string | undefined;
  let prodProjectRef: string | undefined;
  let supabaseUrl: string | undefined;
  let serviceRoleKey: string | undefined;
  let managementApiToken: string | undefined;
  let prodVersions: string[] = DEFAULT_PROD_VERSIONS;
  let prodFacts: ProdFactsData | undefined;
  let format: 'json' | 'text' = 'json';

  for (let i = 0; i < argv.length; i++) {
    const arg = argv[i];
    const next = argv[i + 1];
    switch (arg) {
      case '--project-ref':
        projectRef = next;
        i++;
        break;
      case '--prod-project-ref':
        prodProjectRef = next;
        i++;
        break;
      case '--supabase-url':
        supabaseUrl = next;
        i++;
        break;
      case '--service-role-key':
        serviceRoleKey = next;
        i++;
        break;
      case '--management-api-token':
        managementApiToken = next;
        i++;
        break;
      case '--prod-versions':
        prodVersions = next?.split(',').map((v) => v.trim()).filter(Boolean) ?? DEFAULT_PROD_VERSIONS;
        i++;
        break;
      case '--prod-facts':
        try {
          prodFacts = JSON.parse(next) as ProdFactsData;
        } catch { /* ignore malformed JSON */ }
        i++;
        break;
      case '--format':
        format = next === 'text' ? 'text' : 'json';
        i++;
        break;
    }
  }

  return { projectRef, prodProjectRef, supabaseUrl, serviceRoleKey, managementApiToken, prodVersions, prodFacts, format };
}

// ---------------------------------------------------------------------------
// Text formatter
// ---------------------------------------------------------------------------

function formatText(report: PreflightReport): string {
  const lines: string[] = [];
  lines.push('='.repeat(60));
  lines.push(`Staging Honesty Preflight — ${report.environment_type.toUpperCase()}`);
  lines.push(`Project: ${report.staging_project_ref}`);
  lines.push(`Time:    ${report.timestamp}`);
  lines.push('='.repeat(60));
  lines.push('');

  for (const check of report.checks) {
    const icon = check.passed ? '[PASS]' : '[FAIL]';
    lines.push(`${icon} ${check.name}`);
    lines.push(`       ${check.details}`);
  }

  if (report.artifact_rows.length > 0) {
    lines.push('');
    lines.push('Artifact rows:');
    for (const row of report.artifact_rows) {
      lines.push(`  version=${row.version}  name=${row.name}`);
    }
  }

  if (report.missing_from_staging.length > 0) {
    lines.push('');
    lines.push(`Missing from staging: ${report.missing_from_staging.join(', ')}`);
  }
  if (report.extra_vs_prod.length > 0) {
    lines.push('');
    lines.push(`Extra vs prod: ${report.extra_vs_prod.join(', ')}`);
  }

  lines.push('');
  return lines.join('\n');
}

