// Pure classification functions (exported for testing)
// ---------------------------------------------------------------------------

/**
 * Classify a single migration row. Returns null if the row looks canonical,
 * or an ArtifactClassification describing why it is suspect.
 */
export function classifyMigrationRow(row: MigrationRow): ArtifactClassification | null {
  const { version, name } = row;

  // The init row is always canonical.
  if (version === '00000000000000') return null;

  // Timestamp-format version (14+ digits, but not the init row).
  if (TIMESTAMP_VERSION_RE.test(version)) {
    return { row, reason: `timestamp version (${version}) — likely preview-branch or staging-only` };
  }

  // Known staging/PR prefixes in name.
  const lowerName = name.toLowerCase();
  for (const prefix of STAGING_NAME_PREFIXES) {
    if (lowerName.startsWith(prefix)) {
      return { row, reason: `name starts with ${prefix}` };
    }
  }

  return null;
}

/**
 * Generic duplicate finder — returns values of `field` that appear more than once.
 */
function findDuplicates(rows: MigrationRow[], field: keyof MigrationRow): string[] {
  const counts = new Map<string, number>();
  for (const row of rows) {
    const val = row[field];
    counts.set(val, (counts.get(val) ?? 0) + 1);
  }
  return [...counts.entries()].filter(([, count]) => count > 1).map(([val]) => val);
}

/**
 * Find migration names that appear more than once.
 */
export function findDuplicateNames(rows: MigrationRow[]): string[] {
  return findDuplicates(rows, 'name');
}

/**
 * Find migration versions that appear more than once.
 */
export function findDuplicateVersions(rows: MigrationRow[]): string[] {
  return findDuplicates(rows, 'version');
}

/**
 * Detect rows that match the hardcoded known-artifact list.
 * Also flags duplicate occurrences of KNOWN_DUPLICATE_NAME.
 */
export function detectKnownArtifacts(rows: MigrationRow[]): MigrationRow[] {
  const artifacts: MigrationRow[] = [];

  // Track how many times the known-duplicate name appears.
  let duplicateNameCount = 0;

  for (const row of rows) {
    if (KNOWN_ARTIFACT_NAMES.has(row.name)) {
      artifacts.push(row);
      continue;
    }
    if (row.name === KNOWN_DUPLICATE_NAME) {
      duplicateNameCount++;
      if (duplicateNameCount > 1) {
        // Flag the second (and subsequent) occurrence.
        artifacts.push(row);
      }
    }
  }

  return artifacts;
}

export function isSupabaseMigrationsSchemaUnavailable(
  error: { code?: string; message?: string } | null | undefined,
): boolean {
  const code = error?.code?.toUpperCase();
  const message = error?.message?.toLowerCase() ?? '';
  return code === 'PGRST106'
    || message.includes('schema must be one of')
    || (message.includes('supabase_migrations') && message.includes('invalid schema'));
}

function normalizeMigrationVersion(value: unknown): string | null {
  const raw = String(value ?? '').trim();
  if (!raw) return null;
  if (raw === '00000000000000') return raw;
  if (/^\d+$/.test(raw) && raw.length < 14) {
    return raw.padStart(4, '0');
  }
  return raw;
}

export function mapManagementMigrationRows(rows: readonly Record<string, unknown>[]): MigrationRow[] {
  return rows.flatMap((row) => {
    const version = normalizeMigrationVersion(row.version);
    if (!version) return [];
    return [{
      version,
      name: String(row.name ?? ''),
    }];
  });
}

export function mapManagementMigrationVersions(rows: readonly Record<string, unknown>[]): string[] {
  return rows.flatMap((row) => {
    const version = normalizeMigrationVersion(row.version);
    return version ? [version] : [];
  });
}

export function mapManagementProdFacts(
  cronRows: readonly Record<string, unknown>[],
  functionRows: readonly Record<string, unknown>[],
): ProdFactsData {
  const cronJobNamesValue = cronRows[0]?.cron_job_names;
  const cronJobNames = Array.isArray(cronJobNamesValue)
    ? cronJobNamesValue.map(String)
    : [];
  return {
    cronJobNames,
    functionExists: functionRows[0]?.function_exists === true,
  };
}

/**
 * Compare staging migration versions against the prod ledger.
 */
export function computeProdDivergence(
  rows: MigrationRow[],
  prodVersions: string[],
): { missingFromStaging: string[]; extraVsProd: string[] } {
  const stagingVersions = new Set(rows.map((r) => r.version));
  const prodSet = new Set(prodVersions);

  const missingFromStaging = prodVersions.filter((v) => !stagingVersions.has(v));
  const extraVsProd = [...stagingVersions].filter((v) => !prodSet.has(v));

  return { missingFromStaging, extraVsProd };
}

/**
 * Check org topology: distinguish single-tenant prod from multi-org staging seeds.
 * Requires org-scoped fixtures (non-seed orgs) when seed orgs are present.
 */
export function checkOrgTopology(data: OrgTopologyData): CheckResult {
  if (data.totalOrgs === 0) {
    return {
      name: 'org_topology',
      passed: false,
      details: 'No organizations found — staging lacks seed data.',
    };
  }

  if (data.seedOrgs === 0) {
    return {
      name: 'org_topology',
      passed: true,
      details: `${data.totalOrgs} org(s), no staging seed orgs — prod-like single-tenant topology.`,
    };
  }

  const fixtureOrgs = data.totalOrgs - data.seedOrgs;
  if (fixtureOrgs > 0) {
    return {
      name: 'org_topology',
      passed: true,
      details: `${data.totalOrgs} org(s) total (${data.seedOrgs} seed, ${fixtureOrgs} org-scoped fixture(s)).`,
    };
  }

  return {
    name: 'org_topology',
    passed: false,
    details: `All ${data.totalOrgs} org(s) are seed-prefixed — no org-scoped fixtures for connector work.`,
  };
}

/**
 * Verify prod facts from the 2026-05-06 correction:
 *   - pg_cron has vacuum-anchors job
 *   - refresh_pipeline_dashboard_cache() exists
 *   - refresh-pipeline-dashboard-cache is scheduled per the SCRUM-1708 cadence
 */
export function checkProdFacts(data: ProdFactsData): CheckResult {
  const issues: string[] = [];

  if (!data.cronJobNames.includes('vacuum-anchors')) {
    issues.push('pg_cron vacuum-anchors job missing');
  }

  if (!data.functionExists) {
    issues.push('refresh_pipeline_dashboard_cache() function missing');
  }

  if (!data.cronJobNames.includes('refresh-pipeline-dashboard-cache')) {
    issues.push('refresh-pipeline-dashboard-cache job missing');
  }

  return {
    name: 'prod_facts',
    passed: issues.length === 0,
    details: issues.length === 0
      ? 'Prod facts verified: vacuum-anchors scheduled, refresh_pipeline_dashboard_cache exists, refresh-pipeline-dashboard-cache scheduled.'
      : `Prod facts divergence: ${issues.join('; ')}.`,
  };
}

/**
 * Classify an org name as a staging seed org (case-insensitive prefix match).
 */
export function isOrgSeedName(name: string): boolean {
  const lower = name.toLowerCase();
  return SEED_ORG_PREFIXES.some((p) => lower.startsWith(p));
}

