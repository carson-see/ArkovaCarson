#!/usr/bin/env -S npx tsx
/**
 * SCRUM-1668 addendum — staging-honesty preflight.
 *
 * Queries a Supabase staging database and reports whether the environment
 * is clean or has migration artifacts left over from prior PR soaks,
 * purges, or fixture seeding.
 *
 * Usage:
 *   npx tsx scripts/ci/staging-honesty-preflight.ts \
 *     --project-ref ujtlwnoqfhtitcmsnrpq \
 *     --format json
 *
 * Connection:
 *   --project-ref  Supabase project ref (or STAGING_PROJECT_REF env var).
 *                  URL derived as https://<ref>.supabase.co
 *   --prod-project-ref  Production Supabase project ref (or PROD_PROJECT_REF
 *                  env var). When paired with a Management API token, the
 *                  prod ledger and prod cron facts are queried live.
 *   --supabase-url Override the derived URL directly.
 *   --service-role-key  Service role key (or SUPABASE_SERVICE_ROLE_KEY env).
 *   --management-api-token  Supabase Management API token (or
 *                  SUPABASE_ACCESS_TOKEN / SUPABASE_MANAGEMENT_API_TOKEN env)
 *                  used as a read-only fallback when supabase_migrations is
 *                  hidden from PostgREST.
 *
 * Checks:
 *   1. PR-only / staging-only rows (timestamp versions, pr695_/pr697_/
 *      staging_purge_/staging_only_ prefixes)
 *   2. Duplicate migration names
 *   3. Duplicate migration versions
 *   4. Known artifact rows (hardcoded list)
 *   5. Missing SUBMITTED anchors
 *   6. Prod ledger divergence
 *   7. Org topology (single-tenant prod vs multi-org staging seeds)
 *   8. Prod facts (pg_cron vacuum-anchors, refresh_pipeline_dashboard_cache)
 *
 * Exit 0 = all checks pass (clean_mirror).
 * Exit 1 = at least one check failed.
 */

import { createClient } from '@supabase/supabase-js';
import { resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

// ---------------------------------------------------------------------------
// Types
// ---------------------------------------------------------------------------

export interface MigrationRow {
  version: string;
  name: string;
}

export interface ArtifactClassification {
  row: MigrationRow;
  reason: string;
}

export interface CheckResult {
  name: string;
  passed: boolean;
  details: string;
}

export type EnvironmentType = 'clean_mirror' | 'soak_artifact' | 'fixture_seeded';

export interface PreflightReport {
  environment_type: EnvironmentType;
  staging_project_ref: string;
  timestamp: string;
  checks: CheckResult[];
  artifact_rows: MigrationRow[];
  missing_from_staging: string[];
  extra_vs_prod: string[];
}

export interface OrgTopologyData {
  totalOrgs: number;
  seedOrgs: number;
}

export interface ProdFactsData {
  cronJobNames: string[];
  functionExists: boolean;
}

export interface ParsedArgs {
  projectRef: string | undefined;
  prodProjectRef: string | undefined;
  supabaseUrl: string | undefined;
  serviceRoleKey: string | undefined;
  managementApiToken: string | undefined;
  prodVersions: string[];
  prodFacts: ProdFactsData | undefined;
  format: 'json' | 'text';
}

