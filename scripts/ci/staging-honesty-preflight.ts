const DEFAULT_PROD_VERSIONS = ['00000000000000', '0294', '0295', '0296', '0297'];

/** Names that are categorically known artifacts from prior soak runs. */
const KNOWN_ARTIFACT_NAMES = new Set([
  'pr695_0292',
  'pr695_0293',
  'pr697_0290',
  'staging_purge_v5',
  'staging_purge_v6',
]);

/** Name that becomes an artifact only when it appears more than once. */
const KNOWN_DUPLICATE_NAME = '0294_refund_org_credit';

/**
 * Prefixes in migration names that indicate staging-only or PR-only rows.
 * Checked with `startsWith` against the lowercased name.
 */
const STAGING_NAME_PREFIXES = ['pr695_', 'pr697_', 'staging_purge_', 'staging_only_'];

/**
 * A version with 14+ digits is a Supabase timestamp migration (yyyyMMddHHmmss)
 * generated by preview branches or manual `supabase migration new`. These are
 * never canonical.
 *
 * Exception: '00000000000000' is the init row and always canonical.
 */
const TIMESTAMP_VERSION_RE = /^\d{14,}$/;

/** Prefixes in org names that indicate staging seed data (case-insensitive). */
const SEED_ORG_PREFIXES = ['stg', 'staging_seed_', 'test_org_'];

const MANAGEMENT_API_BASE_URL = 'https://api.supabase.com/v1';
const MIGRATION_LEDGER_QUERY = `
  select version, name
  from supabase_migrations.schema_migrations
  order by version asc
`;
const MIGRATION_VERSION_QUERY = `
  select version
  from supabase_migrations.schema_migrations
  order by version asc
`;
const CRON_JOB_NAMES_QUERY = `
  select coalesce(jsonb_agg(jobname order by jobname), '[]'::jsonb) as cron_job_names
  from cron.job
`;
const REFRESH_FUNCTION_EXISTS_QUERY = `
  select to_regprocedure('public.refresh_pipeline_dashboard_cache()') is not null as function_exists
`;

